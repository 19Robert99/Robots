package com.lesson5.cw.tasks.misha;

public class Main {
    public static void main(String[] args) {
        for (int firstNumb = 1; firstNumb < 10; firstNumb++) {
            for (int secondNumb = 1; secondNumb < 10; secondNumb++) {
                int result = firstNumb * secondNumb;
                System.out.print(result + "\t");
            }
            System.out.println();
        }
    }
}




