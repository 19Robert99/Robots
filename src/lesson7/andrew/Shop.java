package lesson7.andrew;

public class Shop {
    private int area;
    private int quantityOfGoods;
    private int quantityOfWorkers;
    public int getArea() {
        return area;
    }

    public void setArea(int area) {
        this.area = area;
    }

    public int getQuantityOfGoods() {
        return quantityOfGoods;
    }

    public void setQuantityOfGoods(int quantityOfGoods) {
        this.quantityOfGoods = quantityOfGoods;
    }
    public int getQuantityOfWorkers() {
        return quantityOfWorkers;
    }

    public void setQuantityOfWorkers(int quantityOfWorkers) {
        this.quantityOfWorkers = quantityOfWorkers;
    }

}

